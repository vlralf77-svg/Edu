// 한글 자모 획순 데이터.
// 각 글자는 '획'의 배열이고, 각 획은 0~100 좌표(y는 아래로)의 점 배열이다.
// 점 순서 = 쓰는 방향(시범 애니메이션/화살표에 사용).

export type Point = [number, number];
export type Stroke = Point[];

// 원(ㅇ, ㅎ의 동그라미)을 폴리라인으로 근사 — 12시에서 시작해 시계방향
export function circle(cx: number, cy: number, r: number, seg = 20): Stroke {
  const pts: Stroke = [];
  for (let i = 0; i <= seg; i++) {
    const a = -Math.PI / 2 + (i / seg) * Math.PI * 2;
    pts.push([cx + r * Math.cos(a), cy + r * Math.sin(a)]);
  }
  return pts;
}

